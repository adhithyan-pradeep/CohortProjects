class Arithmetic
{
    public static void main(String[] args) {
        int a = 2 + 2;
        System.out.println(a);
        int b = a - 2;
        System.out.println(b);
        int c = 10 * b;
        System.out.println(c);
        int d = c / 4;
        System.out.println(d);
    }
}